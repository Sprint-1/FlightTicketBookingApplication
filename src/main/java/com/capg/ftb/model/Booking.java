package com.capg.ftb.model;

import java.math.BigInteger;
import java.time.LocalDate;
import java.util.List;

import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.OneToMany;
import javax.persistence.OneToOne;
import javax.persistence.Table;
import javax.validation.constraints.Min;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

@Entity
@Table(name="Booking")
@ApiModel(value="Booking Entity")
public class Booking {
	
	@Id
	@GeneratedValue
	@ApiModelProperty(notes = "Autogenerated", required = true, position=1)
	private BigInteger bookingId;
	
	
	@ApiModelProperty(notes = "Unique Name of user", required = true, position=2)
	private String userName;
	
	@Column
	@ApiModelProperty(notes = "Current date of Booking", required = false, position=3)
	private LocalDate bookingDate;
	
	@OneToOne(cascade=CascadeType.ALL,fetch=FetchType.EAGER)
	@JoinColumn(referencedColumnName="pnrNumber") 
	@ApiModelProperty(notes = "Pasenger", required = false, position=4)
	private Passenger passengerList;
	
	@Column
	@ApiModelProperty(notes = "Caluculated Automaticaly based on NoOfPasengers", required = false, position=5)
	private double ticketCost;
	
	@Column
	@ApiModelProperty(notes = "Scheduled Flight Id of Flight", required =true, position=6)
	private BigInteger scheduledFlightId;
	
	@Column
	@Min(value=1,message="atleast one passenger required")
	@ApiModelProperty(notes = "minimum 1 pasenger required", required = false, position=7)
	private int noOfPassangers;
	
	public BigInteger getBookingId() {
		return bookingId;
	}
	public void setBookingId(BigInteger bookingId) {
		this.bookingId = bookingId;
	}
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userId) {
		this.userName = userId;
	}
	
	public LocalDate getBookingDate() {
		return bookingDate;
	}
	public void setBookingDate(LocalDate bookingDate) {
		this.bookingDate = bookingDate;
	}
	
	public Passenger getPassengerList() {
		return passengerList;
	}
	public void setPassengerList(Passenger passengerList) {
		this.passengerList = passengerList;
	}
	
	public double getTicketCost() {
		return ticketCost;
	}
	public void setTicketCost(double ticketCost) {
		this.ticketCost = ticketCost;
	}
	
	
	public Booking() {
		super();
	}
	public int getNoOfPassangers() {
		return noOfPassangers;
	}
	public void setNoOfPassangers(int noOfPassangers) {
		this.noOfPassangers = noOfPassangers;
	}
	
	public Booking(BigInteger bookingId,String userName,LocalDate bookingDate,Passenger passengerList,double ticketCost,BigInteger scheduledFlightId,int noOfPassangers) {
		super();
		this.bookingId=bookingId;
		this.userName=userName;
		this.bookingDate=bookingDate;
		this.passengerList=passengerList;
		this.ticketCost=ticketCost;
		this.scheduledFlightId=scheduledFlightId;
		this.noOfPassangers=noOfPassangers;
	}
	public BigInteger getScheduledFlightId() {
		return scheduledFlightId;
	}
	public void setScheduledFlightId(BigInteger scheduledFlightId) {
		this.scheduledFlightId = scheduledFlightId;
	}
		
}

